    /**
     * Transforma de acceso del XML a acceso de la aplicación
     * */
	public static String getAccessTypeName(String accessOfXml){
		if(accessOfXml.equalsIgnoreCase(ACCESS_VIEW)){
			return Constants.ACCESS_VIEW_NAME;
		}else if(accessOfXml.equalsIgnoreCase(ACCESS_SET)){
			return Constants.ACCESS_SET_NAME;
		}else if(accessOfXml.equalsIgnoreCase(ACCESS_DEL)){
			return Constants.ACCESS_DEL_NAME;
		}else if(accessOfXml.equalsIgnoreCase(ACCESS_NEW)){
			return Constants.ACCESS_NEW_NAME;
		}else if(accessOfXml.equalsIgnoreCase(ACCESS_FIND)){
			return Constants.ACCESS_FIND_NAME;
		}
		return null;
	}
