package dynagent.common.basicobjects;

public class ListenerUtask {

	private Integer utask;
	private Integer rgb;
	private Integer updatePeriod;

	public Integer getUtask() {
		return utask;
	}

	public void setUtask(Integer utask) {
		this.utask = utask;
	}

	public Integer getRgb() {
		return rgb;
	}

	public void setRgb(Integer rgb) {
		this.rgb = rgb;
	}

	public Integer getUpdatePeriod() {
		return updatePeriod;
	}

	public void setUpdatePeriod(Integer updatePeriod) {
		this.updatePeriod = updatePeriod;
	}
	
	
}
